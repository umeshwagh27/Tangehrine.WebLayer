        #region Method
        public async Task<IActionResult> Index()
        {
            ViewBag.Html = HttpContext.Session.GetString("string");
            var masterData = await _masterService.GetMasterList();
            return View("MasterIndex", masterData);
        }
        public async Task<IActionResult> GetMedicine(JQueryDataTableParamModel param)
        {
            using (var txscope = new TransactionScope(TransactionScopeAsyncFlowOption.Enabled))
            {
                try
                {
                    var parameters = CommonMethod.GetJQueryDatatableParamList(param, GetSortingColumnName(1)).Parameters;
                    var allList = await _masterService.GetMedicineList(parameters.ToArray());

                    var total = allList.FirstOrDefault()?.TotalRecords ?? 0;
                    return Json(new
                    {
                        param.sEcho,
                        iTotalRecords = total,
                        iTotalDisplayRecords = total,
                        aaData = allList
                    });
                }
                catch (Exception ex)
                {
                    return Json(new
                    {
                        param.sEcho,
                        iTotalRecords = 0,
                        iTotalDisplayRecords = 0,
                        aaData = ""
                    });
                }
            }
        }

